package homework2;

import java.util.Arrays;

public class Task2_2 {
    /*
    Задача 2:
Используя while вывести все числа от 0 до 25 в одну строку через пробел.
     */
    public static void main(String[] args) {
        int a = 0;
        while (a <= 25) {
            System.out.print(a + " ");
            a++;
        }
    }
}
